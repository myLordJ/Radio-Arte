		<h1>About Us</h1>
		<h2>WHO WE ARE</h2>
		<p>Radio Arte, 90.5 WRTE-FM, is a Latino-owned, bilingual, youth-driven public radio station that works to advance the voices of our multi-layered society. 
			Radio Arte does this by promoting socially conscious journalism, providing media-literacy training; and creating programming that showcases music, 
			issues, and events that are not featured prominently by mainstream media. Our guiding principals have been to:
		</p>
		<ol>
			<li>Create and deliver high quality experiential learning opportunities that enable Latino youth to competencies and life skills to pursue careers 
				of their choice, particularly journalism and media.</li>
			<li>Build a strong national network of Spanish language youth media creators.</li>
			<li>Support Latino youth as they become resources to the larger community through their involvement in media and civic engagement.</li>
		</ol>
		
		
		<h2>OUR HISTORY</h2>
		<p>
			The National Museum of Mexican Art (NMMA) acquired this Class D radio station in 1996 from the Boys & Girls Club of Chicago (BGCC), and has since 
			supported the various initiatives that have catapulted Radio Arte into a national award winning institution.<br/><br/>  
			Since 1997, Radio Arte has offered a comprehensive bilingual one-year media-training program to hundreds of youth (ages 15-21). Through Radio 
			Arte's training, young adults receive instruction in journalism basics and are taught to analyze a variety of social justice issues; as well, 
			they gain hands-on technical experience in radio production. Radio Arte's public affairs programming is driven by young people that consider the 
			airwaves a tool of empowerment for themselves and their communities.
		</p>
		
		<h2>OUR FORMAT</h2>
		<h3>Latin Alternative</h3>
		<p>
			Since the mid-90's, Radio Arte has positioned itself as the only radio station in Chicago that has fully embraced the eclectic range of Latin music. 
			Defying the traditional expectations imposed upon Latino culture, Radio Arte gave voice to the emerging sounds of Rock en Espa&#241;ol that included ska, 
			electronica, and punk among others.<br/><br/>
			Highlighting local and international artists, Radio Arte embraced acts such as Ely Guerra, Jaguares, Soda Stereo, Maria Fatal, Pastilla, Ley de Hielo, 
			Los Crudos, La Querida de Cortes, and Los Enemigos de Paquita. Radio Arte continues to explore the innovation and independence of the Latin Alternative 
			music, while recognizing the roots of its movement.<br/><br/>
			Our Latin Alternative music programming features cutting edge Spanish language music from the U.S & Latin America, along with retro hits.  Includes 
			informative music segments and a public affairs block featuring syndicated programs, Pacifica's *Democracy Now & Radio Bilingue's *Linea Abierta, 
			from 1-3pm.
		</p>
		
